#include<bits/stdc++.h>
using namespace std;

int sort(int n,int arr[]){
    int min=arr[0];
    for(int i=0;i<n;i++){
    if(min>arr[i]) {
        min=arr[i];
    }
}
return min;
}

int main() {
    int n;
    cin>>n;
    int a[n];
    for(int i=0;i<n;i++) {
        cin>>a[i];
    }
    cout<<"Smallest element is :"<<sort(n,a);
    
    return 0;
}
